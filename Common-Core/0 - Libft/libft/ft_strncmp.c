#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, unsigned int len)
{
	if (!len)
		return (0);
	while (len-- && (*s1 || *s2))
		if (*(unsigned char *)s1++ != *(unsigned char *)s2++)
			return (*(unsigned char *)--s1 - *(unsigned char *)--s2);
	return (0);
}
